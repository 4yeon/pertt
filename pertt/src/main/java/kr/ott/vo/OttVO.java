package kr.ott.vo;

public class OttVO {
	private int ott_num;
	private String ott_name;
	private String ott_content;
	private String ott_url;
	private int ott_rank_num;
	private int price;
	private int usability;
	private int quality;
	private String ott_review;
	private int user_num;
	
	
	public int getOtt_num() {
		return ott_num;
	}
	public void setOtt_num(int ott_num) {
		this.ott_num = ott_num;
	}
	public String getOtt_name() {
		return ott_name;
	}
	public void setOtt_name(String ott_name) {
		this.ott_name = ott_name;
	}
	public String getOtt_content() {
		return ott_content;
	}
	public void setOtt_content(String ott_content) {
		this.ott_content = ott_content;
	}
	public String getOtt_url() {
		return ott_url;
	}
	public void setOtt_url(String ott_url) {
		this.ott_url = ott_url;
	}
	public int getOtt_rank_num() {
		return ott_rank_num;
	}
	public void setOtt_rank_num(int ott_rank_num) {
		this.ott_rank_num = ott_rank_num;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getUsability() {
		return usability;
	}
	public void setUsability(int usability) {
		this.usability = usability;
	}
	public int getQuality() {
		return quality;
	}
	public void setQuality(int quality) {
		this.quality = quality;
	}
	public String getOtt_review() {
		return ott_review;
	}
	public void setOtt_review(String ott_review) {
		this.ott_review = ott_review;
	}
	public int getUser_num() {
		return user_num;
	}
	public void setUser_num(int user_num) {
		this.user_num = user_num;
	}
}
